const QUALITY_MULT: Record<string, [number, number]> = {
  Ужасный: [0.3, 0.5],
  Плохой: [0.5, 0.8],
  Средний: [1, 1],
  Хороший: [1.2, 1.5],
  Прекрасный: [1.5, 2.5],
};

function getGoodsForCategory(category: string): string[] {
  return GOODS_BY_CATEGORY[category] ?? GOODS_BY_CATEGORY.Коробейник;
}

const PRICE_KEYS = ['price', 'cost', 'стоимость', 'цена', 'value', 'зм'];

function getPriceFromData(data: Record<string, unknown>): number | null {
  for (const key of PRICE_KEYS) {
    const v = data[key];
    if (typeof v === 'number' && v >= 0) return Math.floor(v);
    if (typeof v === 'string') {
      const n = parseInt(v.replace(/\D/g, ''), 10);
      if (!Number.isNaN(n)) return n;
    }
  }
  return null;
}

export function generateMerchantGoods(
  category: MerchantCategory,
  quality: MerchantQuality,
  count: number = 12
): MerchantGoodsItem[] {
  const marketItems = MARKET_DATA[category]?.[quality];
  if (marketItems && marketItems.length > 0) {
    const shuffled = [...marketItems].sort(() => Math.random() - 0.5);
    const selected = shuffled.slice(0, Math.min(count, marketItems.length));
    return selected.map((it) => ({
      name: it.name,
      price: Math.round(it.priceCopper / 100 * 10) / 10,
      priceText: it.priceText,
      qty: it.qty || '1',
      note: it.note || undefined,
    }));
  }
  const pool = getGoodsForCategory(category);
  const [multMin, multMax] = QUALITY_MULT[quality] ?? [1, 1];
  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  const selected = shuffled.slice(0, Math.min(count, pool.length));
  return selected.map((name) => {
    const base = 5 + Math.random() * 45;
    const mult = multMin + Math.random() * (multMax - multMin);
    const price = Math.max(1, Math.floor(base * mult) * 10);
    return {
      name,
      price,
      qty: 1 + Math.floor(Math.random() * 4),
    };
  });
}

/** Генерация товаров из записей справочника (название + опциональная цена в data) */
export function generateMerchantGoodsFromEntries(
  entries: Array<{ name: string; data: Record<string, unknown> }>,
  quality: MerchantQuality,
  count: number
): MerchantGoodsItem[] {
  if (entries.length === 0) return [];
  const [multMin, multMax] = QUALITY_MULT[quality] ?? [1, 1];
  const shuffled = [...entries].sort(() => Math.random() - 0.5);
  const selected = shuffled.slice(0, Math.min(count, entries.length));
  return selected.map((entry) => {
    const basePrice = getPriceFromData(entry.data);
    const mult = multMin + Math.random() * (multMax - multMin);
    const price = basePrice != null
      ? Math.max(1, Math.floor(basePrice * mult))
      : Math.max(1, Math.floor((5 + Math.random() * 45) * mult) * 10);
    return {
      name: entry.name,
      price,
      qty: 1 + Math.floor(Math.random() * 4),
    };
  });
}
